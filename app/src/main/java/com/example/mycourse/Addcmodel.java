package com.example.mycourse;

public class Addcmodel {
     public String du,ve,da,de,ti;

    public Addcmodel() {
    }

    public Addcmodel(String du, String ve, String da, String de, String ti) {
        this.du = du;
        this.ve = ve;
        this.da = da;
        this.de = de;
        this.ti = ti;
    }

    public String getDu() {
        return du;
    }

    public void setDu(String du) {
        this.du = du;
    }

    public String getVe() {
        return ve;
    }

    public void setVe(String ve) {
        this.ve = ve;
    }

    public String getDa() {
        return da;
    }

    public void setDa(String da) {
        this.da = da;
    }

    public String getDe() {
        return de;
    }

    public void setDe(String de) {
        this.de = de;
    }

    public String getTi() {
        return ti;
    }

    public void setTi(String ti) {
        this.ti = ti;
    }
}
